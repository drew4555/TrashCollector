using Microsoft.AspNet.Identity;
using System;
using System.Collections.Generic;
using System.Data;
using System.Data.Entity;
using System.Linq;
using System.Net;
using System.Web;
using System.Web.Mvc;
using TrashCollector.Models;

namespace TrashCollector.Controllers
{
    public class ClientsController : Controller
    {

        ApplicationDbContext db;
        public ClientsController()
        {
            db = new ApplicationDbContext();
        }
        // GET: Clients
        public ActionResult Index()
        {
            return View(db.Clients.ToList());
        }

        // GET: Clients/Details/5
        public ActionResult Details(string id)
        {
            id = User.Identity.GetUserId();

            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Client client = db.Clients.Find(id);
            if (client == null)
            {
                return HttpNotFound();
            }
            return View(client);
        }

        // GET: Clients/Create
        public ActionResult Create()
        {
            //Setupdaysofweek();
            ClientViewModel cvm = new ClientViewModel();

            List<string> daysofweek = new List<string> { "monday", "tuesday", "wednesday", "thursday", "friday" };
            var items = daysofweek;
            if (items != null)
            {
                ViewBag.DaysOfWeek = items;
            }

            cvm.Client = new Client();
            cvm.DaysOfWeek = new DaysofWeek();

            return View(cvm);
        }

        // POST: Clients/Create
        // To protect from overposting attacks, please enable the specific properties you want to bind to, for 
        // more details see https://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Create([Bind(Include = "Client, DaysOfWeek")] ClientViewModel clientViewModel)
        {
            if (ModelState.IsValid)
            {
                db.Clients.Add(clientViewModel.Client);
                db.SaveChanges();
                clientViewModel.DaysOfWeek.CustomerId = clientViewModel.Client.Id;
                db.DaysofWeeks.Add(clientViewModel.DaysOfWeek);
                db.SaveChanges();
                return RedirectToAction("Create", "Addresses", new { id = clientViewModel.Client.Id });
            }

            return View(clientViewModel);
        }

        // GET: Clients/Edit/5
        public ActionResult Edit(string id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Client client = db.Clients.Find(id);
            if (client == null)
            {
                return HttpNotFound();
            }
            return View(client);
        }

        // POST: Clients/Edit/5
        // To protect from overposting attacks, please enable the specific properties you want to bind to, for 
        // more details see https://go.microsoft.com/fwlink/?LinkId=317598.
        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Edit([Bind(Include = "Id,First_Name,Last_Name,Address,Address_Line_2,City,State,Zip_Code,Payment_Info,Collection_Day,User_Name,Password,Extra_Pickup")] Client client)
        {
            if (ModelState.IsValid)
            {
                db.Entry(client).State = EntityState.Modified;
                db.SaveChanges();
                return RedirectToAction("Index");
            }
            return View(client);
        }

        // GET: Clients/Delete/5
        public ActionResult Delete(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            Client client = db.Clients.Find(id);
            if (client == null)
            {
                return HttpNotFound();
            }
            return View(client);
        }

        // POST: Clients/Delete/5
        [HttpPost, ActionName("Delete")]
        [ValidateAntiForgeryToken]
        public ActionResult DeleteConfirmed(int id)
        {
            Client client = db.Clients.Find(id);
            db.Clients.Remove(client);
            db.SaveChanges();
            return RedirectToAction("Index");
        }
